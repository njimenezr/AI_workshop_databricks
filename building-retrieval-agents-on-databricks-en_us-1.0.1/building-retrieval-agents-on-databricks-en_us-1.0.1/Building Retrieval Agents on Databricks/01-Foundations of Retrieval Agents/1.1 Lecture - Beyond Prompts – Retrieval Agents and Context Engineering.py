# MAGIC %md
# MAGIC ## B. Retrieval Augmented Generation (RAG)
# MAGIC
# MAGIC While prompt engineering optimizes *how* a model responds, it cannot address the fundamental issue of *what* a model knows. To overcome the limitations of frozen training data and hallucination, we must shift our architectural approach from relying on internal memory to leveraging external context. In this section, we'll introduce Retrieval Augmented Generation (RAG), the critical framework that bridges the gap between a model's pretrained knowledge and your proprietary data.
# MAGIC
# MAGIC **RAG vs. Retrieval Agent:** 
# MAGIC RAG refers to the architectural pattern of coupling retrieval with generation, independent of any specific tooling or agent logic. **Retrieval agents**, by contrast, are concrete implementations that operationalize this pattern—handling query routing, retrieval orchestration, and context assembly within real systems.

# COMMAND ----------

# MAGIC %md
# MAGIC ### B1. Defining RAG
# MAGIC
# MAGIC To solve the knowledge boundaries we defined earlier, we shift from a memory-based approach to a context-based architecture known as **Retrieval Augmented Generation (RAG)**. RAG injects proprietary or real-time data into the prompt, enabling the model to respond based on provided facts rather than its internal memory.
# MAGIC
# MAGIC The RAG process consists of three key stages:
# MAGIC
# MAGIC - **Retrieval:** The system searches a knowledge base (indexed via **Mosaic AI Vector Search**) for relevant data chunks
# MAGIC - **Augmentation:** The system injects these chunks into the context window
# MAGIC - **Generation:** The model synthesizes an answer using *only* the injected data

# COMMAND ----------

# MAGIC %md
# MAGIC ### B2. The Context Challenge
# MAGIC
# MAGIC While RAG solves the knowledge gap, it introduces a new challenge: **Context Rot**. Early RAG implementations often failed because developers would simply retrieve large volumes of documents and paste them into the prompt. This approach overwhelms the model, leading to:
# MAGIC
# MAGIC - **Context Poisoning:** The inclusion of irrelevant or conflicting information that confuses the model
# MAGIC - **Lost in the Middle:** The tendency of models to ignore information buried in the middle of a long context window, prioritizing data at the very beginning or very end
# MAGIC
# MAGIC These challenges highlight the need for strategic context management, which we'll explore in the next section.

# COMMAND ----------

# MAGIC %md
# MAGIC ## C. Principles of Context Engineering
# MAGIC
# MAGIC Simply retrieving data is not enough; dumping raw information into a prompt often leads to confusion rather than clarity. As we move from basic RAG to production-grade systems, we must treat the context window not as a passive container, but as an engineered environment that actively shapes model behavior. In this section, we'll outline the principles of Context Engineering, emphasizing how to structure, filter, and ground information to ensure reliability and accuracy.

# COMMAND ----------

# MAGIC %md
# MAGIC ### C1. Defining the Context Environment
# MAGIC
# MAGIC **Context Engineering** is the strategic design of the entire input window. It moves beyond writing a single instruction to managing the complete system state. We orchestrate the interplay between the **System Instructions**, **Conversation History**, **Retrieved Data**, and **User Constraints** to ensure the model has exactly the signal it needs to perform the task effectively.

# COMMAND ----------

# MAGIC %md
# MAGIC ### C2. Designing System Prompts
# MAGIC
# MAGIC In a Context Engineering paradigm, the System Prompt is not just a request—it's a behavioral program that defines how the model should operate.
# MAGIC
# MAGIC Key components of an effective system prompt include:
# MAGIC
# MAGIC - **Role Definition:** Explicitly define the persona (e.g., "You are a Databricks Security Architect")
# MAGIC - **Negative Constraints:** Define what the model *cannot* do (e.g., "Do not mention competitor products" or "Do not provide code unless explicitly requested")
# MAGIC - **Output Formatting:** Enforce structured outputs (e.g., JSON, YAML, or Markdown tables) to ensure downstream applications can parse the response deterministically

# COMMAND ----------

# MAGIC %md
# MAGIC ### C3. Strict Grounding and Chunking
# MAGIC
# MAGIC Retrieval must be strictly governed to prevent hallucinations and ensure accuracy.
# MAGIC
# MAGIC Key strategies include:
# MAGIC
# MAGIC - **Grounding Instructions:** Use explicit instructions to bind the model to the retrieved context. For example: *"Answer using ONLY the provided context chunks. If the answer is not present, state 'I do not have that information.'"*
# MAGIC - **Metadata Filtering:** Utilize **Unity Catalog** metadata to filter retrieval *before* it reaches the model. For example, if a user asks about "2024 Revenue," the system should filter chunks where `year=2024` to prevent the model from seeing outdated 2023 data

# COMMAND ----------

# MAGIC %md
# MAGIC ### C4. Managing Multi-Turn State
# MAGIC
# MAGIC For applications involving long conversations (such as Agents), the context window will eventually fill up. Context Engineering requires strategic approaches to manage this state:
# MAGIC
# MAGIC - **Summarization:** Periodically compress the conversation history into a summary of key decisions and facts
# MAGIC - **Moving Window:** Discard the oldest messages to free up token space for new retrieval
# MAGIC - **Selective Persistence:** Determine which pieces of information (e.g., user name, current project ID) must remain in the context permanently versus what can be discarded
# MAGIC
# MAGIC These strategies ensure that we maintain relevant context while staying within token limits.

# COMMAND ----------

# MAGIC %md
# MAGIC ## D. Context Constraints: Token Budgets and Window Limits
# MAGIC
# MAGIC Even the most elegantly engineered context is subject to the hard constraints of compute resources and model architecture. As we scale our applications, we must balance the desire for comprehensive context against the realities of token limits, latency, and operational costs. In this section, we'll examine the economics of the context window, providing strategies to optimize token budgets without sacrificing response quality.

# COMMAND ----------

# MAGIC %md
# MAGIC ### D1. Understanding Context Windows
# MAGIC
# MAGIC Every model has a **Context Window Limit** (e.g., 8k, 32k, or 128k tokens). This represents the hard limit of working memory available to the model.
# MAGIC
# MAGIC The context window consists of:
# MAGIC
# MAGIC - **Input Tokens:** The text we send to the model (instructions + retrieved documents + history)
# MAGIC - **Output Tokens:** The text the model generates
# MAGIC
# MAGIC **The Trade-off:** As we fill the window with more retrieved data, the model's ability to reason degrades (the "Lost in the Middle" phenomenon), and latency increases significantly. Strategic context management is essential for maintaining performance.

# COMMAND ----------

# MAGIC %md
# MAGIC ### D2. Token Economics and Optimization
# MAGIC
# MAGIC Context is not free. **Databricks Foundation Model APIs** (and other providers) charge based on the volume of input and output tokens consumed.
# MAGIC
# MAGIC Key considerations:
# MAGIC
# MAGIC - **Cost Management:** A naive RAG system that retrieves 50 documents for every query will burn through token budgets rapidly
# MAGIC
# MAGIC **Optimization Strategies:**
# MAGIC
# MAGIC - **Just-in-Time Retrieval:** Instead of loading a full manual at the start of a chat, give the Agent a tool to retrieve specific sections only when the user asks a relevant question
# MAGIC - **Reranking:** Use a reranker model to score the top 50 retrieved chunks and only inject the top 3-5 most relevant ones into the final context window
# MAGIC
# MAGIC These strategies help us balance comprehensive context with cost efficiency and performance.

# COMMAND ----------

# MAGIC %md
# MAGIC ## E. Summary
# MAGIC
# MAGIC Moving from Prompt Engineering to Context Engineering represents a fundamental shift from "micro-optimization" to "macro-architecture." While prompts control tone and format, they cannot bridge the knowledge gap inherent in LLMs. RAG architectures solve this by injecting external data, but introduce complexity around context window management. Context Engineering addresses these challenges by rigorously structuring the input, enforcing grounding rules, and managing token budgets to create reliable, cost-effective AI systems.
# MAGIC
# MAGIC **Key Takeaways:**
# MAGIC
# MAGIC 1. **Retrieval Agent Architecture:** Use retrieval components to bridge knowledge gaps, and apply Context Engineering to optimize retrieval agents for performance, reliability, and cost.
# MAGIC 2. **Context is Finite:** Manage the context window like a budget. Use filtering and reranking to maximize the value of every token.
# MAGIC 3. **Grounding is Mandatory:** Strictly instruct the model to use *only* retrieved data and leverage **Unity Catalog** metadata to ensure that data is relevant and secure.

