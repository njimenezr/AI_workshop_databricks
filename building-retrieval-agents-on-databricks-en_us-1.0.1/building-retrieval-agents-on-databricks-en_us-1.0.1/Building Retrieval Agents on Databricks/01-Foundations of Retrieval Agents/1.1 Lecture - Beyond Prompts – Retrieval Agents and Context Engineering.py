# MAGIC %md
# MAGIC ## A. Foundations of Prompt Engineering
# MAGIC
# MAGIC Before we can build complex AI architectures, we must first master the fundamental unit of interaction: the prompt. In this section, we'll explore the art of crafting instructions to guide model behavior, distinguishing between statistical prediction and genuine reasoning capabilities. However, as we refine our prompts, we'll inevitably encounter the hard boundaries of what a model can know based solely on its training data.

# COMMAND ----------

# MAGIC %md
# MAGIC ### A1. Defining Prompt Engineering
# MAGIC
# MAGIC Prompt Engineering is the practice of refining the input text (the prompt) to optimize the output generated by a Large Language Model (LLM). It's a **tactical** discipline focused on the instruction layer. We use techniques such as **Few-Shot Prompting** (providing examples) and **Persona Adoption** (assigning a role) to guide the model's behavior and formatting. Ideally, prompt engineering treats the model as a reasoning engine, guiding it to leverage its pretrained weights to solve problems effectively.

# COMMAND ----------

# MAGIC %md
# MAGIC ### A2. Reasoning vs. Non-Reasoning Models
# MAGIC
# MAGIC Effective prompting requires understanding the capabilities of the underlying model. Let's examine the two primary categories:
# MAGIC
# MAGIC - **Non-Reasoning Models (e.g., Llama 3, GPT-4o):** These models predict the next token based on statistical likelihood. They require explicit guidance, such as **Chain of Thought (CoT)** prompting ("Think step-by-step"), to break down complex logic and avoid rushing to incorrect conclusions.
# MAGIC   
# MAGIC - **Reasoning Models (e.g., OpenAI o1):** These models are trained to generate their own internal chain of thought before producing a final answer. For these models, manual CoT prompting is often redundant or counterproductive. Context engineering for reasoning models focuses on defining the *goal* and *constraints* rather than the *thinking process*.

# COMMAND ----------

# MAGIC %md
# MAGIC ### A3. The Boundaries of Prompting
# MAGIC
# MAGIC Prompt engineering has hard boundaries. No amount of instruction refinement can overcome the following limitations inherent to the model's training data:
# MAGIC
# MAGIC 1. **The Knowledge Cutoff:** The model cannot answer questions about events that occurred after the cutoff date of its training data.
# MAGIC    - *Example Prompt:* `Who won the 2025 Nobel Prize in Physics?`
# MAGIC
# MAGIC 1. **Hallucination:** When asked for specific facts without external references, models often prioritize plausibility over truth, fabricating citations or data points.
# MAGIC    - *Example Prompt:* `Find a scientific reference proving that avocado reduces blood sugar levels`
# MAGIC
# MAGIC 1. **Ambiguity:** Without private context, models default to generic interpretations.
# MAGIC    - *Example Prompt:* `Explain how to secure a lakehouse.` (This triggers advice on physical home security rather than Databricks Data Lakehouse governance.)

