# MAGIC ## Introduction
# MAGIC
# MAGIC As Generative AI applications move from prototypes to production, developers often encounter a performance ceiling. No matter how cleverly we rewrite instructions, we cannot force a model to know facts it was never trained on or prevent it from hallucinating when it lacks critical data. This marks a fundamental transition—from **Prompt Engineering**, which focuses on crafting the query, to **Context Engineering**, which focuses on architecting the entire environment supplied to the model. In this lesson, we'll explore this evolution, defining the boundaries of prompting, introducing the architectural solution of **Retrieval Augmented Generation (RAG)**, and examining the advanced discipline of engineering the context window for reliability and cost-efficiency.
