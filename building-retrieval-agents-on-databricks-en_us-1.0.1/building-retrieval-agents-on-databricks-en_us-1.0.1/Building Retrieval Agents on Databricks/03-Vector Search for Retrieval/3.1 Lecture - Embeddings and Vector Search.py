# MAGIC %md
# MAGIC ### B4. Search Strategies
# MAGIC
# MAGIC Two primary strategies balance accuracy and performance:
# MAGIC
# MAGIC - **K-Nearest Neighbors (KNN):** An exact search method that calculates the distance between the query vector and *every* vector in the database. While highly accurate, it's computationally expensive and doesn't scale well to large datasets—imagine comparing your query against millions of documents one by one.
# MAGIC - **Approximate Nearest Neighbors (ANN):** A strategy that trades a small amount of accuracy for dramatic speed gains. ANN uses sophisticated indexing algorithms like **HNSW** (Hierarchical Navigable Small Worlds) or **FAISS** (Facebook AI Similarity Search) to navigate vector space efficiently, checking only a subset of vectors while still finding highly relevant results.

