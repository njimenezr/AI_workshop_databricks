# MAGIC %md
# MAGIC ### A4. Embedding Alignment
# MAGIC
# MAGIC For retrieval to work effectively, your embedding model must represent both source documents and user queries in the same vector space. If the model trained primarily on long-form documents but your application uses short, informal queries, the vector representations may not align well—leading to poor retrieval results. The best practice is straightforward: **use the same embedding model for both indexing documents and processing queries**. This ensures they exist in the same mathematical space and can be meaningfully compared.

