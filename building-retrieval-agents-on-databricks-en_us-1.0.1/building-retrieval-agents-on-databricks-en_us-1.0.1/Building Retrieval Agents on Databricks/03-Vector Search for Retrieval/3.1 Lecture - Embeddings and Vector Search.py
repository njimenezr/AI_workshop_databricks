# MAGIC ### D1. Product Overview
# MAGIC
# MAGIC **Mosaic AI Vector Search** is a **vector database solution integrated directly into the Databricks Lakehouse**. This scalable, low-latency service stores vector representations of your data alongside their metadata, enabling real-time similarity search through a REST API and Python client. It's purpose-built to optimize retrieval for RAG applications, eliminating the need to manage separate vector database infrastructure.

# COMMAND ----------

# MAGIC %md
# MAGIC ### D2. Delta Sync and Indexing
# MAGIC
# MAGIC One of the most powerful features of Mosaic AI Vector Search is its tight integration with **Delta Lake**. Through the **Delta Sync API**, your vector index automatically syncs with a source Delta table. When you add, update, or delete data in the source table, the vector index updates automatically—ensuring your retrieval system always reflects the most current data without manual intervention. This eliminates the operational burden of keeping embeddings synchronized with your source data.

# COMMAND ----------

# MAGIC %md
# MAGIC ### D3. Management and Ingestion Modes
# MAGIC
# MAGIC Mosaic AI Vector Search offers three flexible approaches for ingesting and managing embeddings, allowing you to choose the level of control that fits your needs:
# MAGIC
# MAGIC 1. **Managed Embeddings (Delta Sync):** You provide a source Delta table containing raw text, and Databricks handles the rest. The system automatically computes embeddings using a configured **Mosaic AI Model Serving** endpoint (such as a Foundation Model API), processes new data, and updates the index—all without requiring you to manage the embedding pipeline.
# MAGIC
# MAGIC 1. **Self-Managed Embeddings (Delta Sync):** You compute embeddings using your own custom pipelines and store them in a Delta table. The Vector Search index syncs with this table, indexing the pre-computed vectors you provide. This gives you full control over the embedding process while still benefiting from automatic synchronization.
# MAGIC
# MAGIC 1. **Direct Access CRUD API:** You can interact directly with the Vector Search index using the REST API or Python SDK. This allows you to insert, update, or delete vectors and metadata directly without relying on an underlying Delta table sync—ideal for real-time applications or custom workflows.
