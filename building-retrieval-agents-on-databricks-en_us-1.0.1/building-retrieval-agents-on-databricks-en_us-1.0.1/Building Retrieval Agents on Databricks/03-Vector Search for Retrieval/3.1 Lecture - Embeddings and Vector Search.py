# MAGIC ### C3. Benefits and Trade-offs
# MAGIC
# MAGIC Reranking introduces important considerations:
# MAGIC
# MAGIC - **Benefits:** Reranking significantly improves the accuracy of context provided to the language model, which directly reduces hallucinations and improves response quality. By refining the initial retrieval results, you ensure the most relevant information reaches the generation stage.
# MAGIC - **Trade-offs:** Adding a reranker increases both latency and cost in your retrieval pipeline. The reranking model must process the query and candidate documents in real-time, adding computational overhead. Balance these costs against the quality improvements for your specific use case.

